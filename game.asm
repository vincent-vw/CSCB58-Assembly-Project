input_object:
	# FUNCTION: input the specified object into the objects array
	# $a0 = number of the object (ex. 1 for platform)
	# $a1 = first point of first row to start inputting object - top-left corner is 0
	# $a2 = first point of last row to input object (inclusive) - bottom-left corner is 16380
	# $a3 = max length of each row (perferably an even value)
	la $t9, OBJECTS	# Load address of objects array
	add $t1, $t9, $a1	# Point of first row to start inputting object (iterator)
	add $t2, $t9, $a2	# Point of last row to input object (inclusive)
	li $t3, 0	# Count length of row so far (iterator)
	move $t4, $a3	# Max length of each row
input_object_loop:	# Outer loop
	li $t3, 0	# Reset counter
	bgt $t1, $t2, input_object_loop_end
input_object_loop_inner:	# Inner loop
	bge $t3, $t4, input_object_loop_inner_end
	sw $a0, 0($t1)	# Store object number into objects array
	addi $t1, $t1, 4	# Go to next position in objects array
	addi $t3, $t3, 1	# Increase iterator
	j input_object_loop_inner	# Jump to start of loop
input_object_loop_inner_end:
	# Figure out how much to increase iterator
	li $t8, 4
	mult $t8, $t4
	mflo $t8
	li $t3, 256
	sub $t8, $t3, $t8
	add $t1, $t1, $t8	# Increase iterator
	j input_object_loop	# Jump to start of loop
input_object_loop_end:
	jr $ra

decrease_health:
	# FUNCTION: decrease health of player only if their last decrease was over a couple seconds ago (buffer time in between)
	# If they have zero health left, they lose
	# It returns 1 if there is still health, 0 if there is no more health
	
	li $v0, 30	# System call for getting time
	syscall		# Time saved in $a0
	la $t0, HEALTHDECTIME	# Get address of variable HEALTHDECTIME
	lw $t0, 0($t0)	# Load value
	sub $t1, $a0, $t0	# $t1 = time elapsed
	blt $t1, 5000, decrease_health_end	# Skip to end if buffer time has not been met
	
	# Set HEALTHDECTIME to current time
	la $t0, HEALTHDECTIME	# Get address of variable HEALTHDECTIME
	li $v0, 30	# System call for getting time
	syscall
	sw $a0, 0($t0)	# Store time into variable
	
	la $t0, HEALTH	# Get the current health
	lw $t0, 0($t0)
	# Push $ra in stack
	addi $sp, $sp, -4
	sw $ra, 0($sp)
	beq $t0, 3, three_health	# If player has three health points, branch
	beq $t0, 2, two_health		# If player has two health points, branch
	beq $t0, 1, one_health		# If player has one health point, branch
