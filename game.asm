	li $v0, 1
	jr $ra
	
check_random_collision:
	# FUNCTION: check if the player collided with specified object
	# $a0 - address of top-left corner of player in objects array
	# $a1 - object number
	# 0 - key
	# 1 - door
	# It returns 1 if true, 0 if false
	move $t1, $a0	# Take the current position of player because that is the location to start checking
	addi $t2, $a0, 780	# Take the current position of player and add 780 because that is the location to stop checking (inclusive)
	beq $a1, 0, check_random_collision_key
	beq $a1, 1, check_random_collision_door
check_random_collision_key:
	li $t9, 7	# 7 = key
	j check_random_collision_loop
check_random_collision_door:
	li $t9, 9	# 9 = door
	j check_random_collision_loop
check_random_collision_loop:
	bgt $t1, $t2, check_random_collision_end
	lw $t3, 0($t1)	# Get the value of the object
	beq $t3, $t9, random_collision_pos	# If the object is hit, branch
	addi $t1, $t1, 4	# Go to element on the right
	lw $t3, 0($t1)	# Get the value of the object
	beq $t3, $t9, random_collision_pos	# If the object is hit, branch
	addi $t1, $t1, 4	# Go to element on the right
	lw $t3, 0($t1)	# Get the value of the object
	beq $t3, $t9, random_collision_pos	# If the object is hit, branch
	addi $t1, $t1, 4	# Go to element on the right
	lw $t3, 0($t1)	# Get the value of the object
	beq $t3, $t9, random_collision_pos	# If the object is hit, branch
	addi $t1, $t1, 244	# Go to next row
	j check_random_collision_loop
check_random_collision_end:	# No object found
	li $v0, 0
	jr $ra
random_collision_pos:	# Object found
	li $v0, 1
	jr $ra
	
	li $v0, 10
      	syscall
