	# FUNCTION: calculate position of the point on the objects array
	# $a0 - x coordinate of point
	# $a1 - y coordinate of point
	# It returns the address of top-left corner of point in objects array
	li $t1, 256
	mult $a1, $t1
	mflo $t1
	li $t2, 4
	mult $a0, $t2
	mflo $t2
	add $t1, $t1, $t2
