	# $a0 - address of top-left corner of player in objects array
	# It returns 1 if true, 0 if false
	# Note: if the player is already overlapping a rock, the function always returns 0
	# Areas to check indicated with x (P indicates the player)
	# xxPPP
	# xxPPPP
	# xxPPP
	# xxP P
