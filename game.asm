	jr $ra

remove_player:
	# FUNCTION: remove the player from the screen
	# $a0 - address of top-left corner of player in bitmap array
	
	li $t0, BASE_ADDRESS	# $t0 = base address for display
	sub $s0, $a0, $t0	# $s0 = address of top-left corner of player with display offset removed
	
	# Draw the areas that the player should be removed from
	addi $sp, $sp, -4	# Create space in stack
	sw $ra, 0($sp)	# Push $ra into stack
	
	# First row of player
	add $a0, $zero, $s0	# Top-left corner of player
	addi $a1, $s0, 12	# Top-right corner of player
	jal draw
	# Second row of player
	addi $s0, $s0, 256
	add $a0, $zero, $s0
	addi $a1, $s0, 12
	jal draw
	# Third row of player
	addi $s0, $s0, 256
	add $a0, $zero, $s0
	addi $a1, $s0, 12
	jal draw
	# Fourth row of player
	addi $s0, $s0, 256
	add $a0, $zero, $s0
	addi $a1, $s0, 12
	jal draw
	
	lw $ra, 0($sp)	# Pop $ra from stack
	addi $sp, $sp, 4	# Delete space in stack
	
	jr $ra

