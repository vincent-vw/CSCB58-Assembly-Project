	beq $t8, 0x64, press_d	# If the value is "d" (ASCII 0x64), go to press_d
	beq $t8, 0x77, press_w	# If the value is "w" (ASCII 0x77), go to press_w
	beq $t8, 0x70, main	# If the value is "p" (ASCII 0x70), restart
	j end_main_loop

